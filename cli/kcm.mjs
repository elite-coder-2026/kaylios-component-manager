#!/usr/bin/env node
import { Command } from "commander";
import inquirer from "inquirer";
import * as fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { spawn } from "node:child_process";

let apiBase = process.env.KCM_API_URL || "http://127.0.0.1:4173/api";
const FRAMEWORKS = ["react", "vue", "vanilla"];

function toKebabCase(input) {
  return String(input || "")
    .trim()
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[\s_]+/g, "-")
    .replace(/[^a-zA-Z0-9-]/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
}

function toPascalCase(input) {
  return toKebabCase(input)
    .split("-")
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join("");
}

function normalizeFramework(input) {
  const raw = String(input || "").trim().toLowerCase();
  if (!raw) return "";
  if (raw.includes("react")) return "react";
  if (raw.includes("vue")) return "vue";
  if (raw.includes("vanilla")) return "vanilla";
  return raw;
}

function run(command, args, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      stdio: "inherit",
      shell: false,
      ...options
    });

    child.on("error", reject);
    child.on("close", (code) => {
      if (code === 0) resolve();
      else reject(new Error(`Command failed (${code}): ${command} ${args.join(" ")}`));
    });
  });
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function request(path, options = {}) {
  const response = await fetch(`${apiBase}${path}`, options);
  let body = null;
  try {
    body = await response.json();
  } catch {
    body = null;
  }

  if (!response.ok || !body?.ok) {
    const reason = body?.error || `Request failed (${response.status})`;
    throw new Error(reason);
  }
  return body;
}

async function createComponent({ framework, component, version, author, number }) {
  const body = await request("/components", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ framework, component, version, author, number })
  });
  console.log(`Created ${framework}/${component}`);
  console.log(`Files: ${body.files.join(", ")}`);
}

async function listComponents() {
  const body = await request("/components");
  const frameworks = Object.keys(body.components);
  for (const fw of frameworks) {
    const items = body.components[fw] || [];
    console.log(`${fw}: ${items.length ? items.join(", ") : "(none)"}`);
  }
}

async function deleteComponent(framework, component) {
  await request(`/components/${framework}/${component}`, { method: "DELETE" });
  console.log(`Deleted ${framework}/${component}`);
}

async function scaffoldReactViteProject(projectDir, projectName, { useTypeScript }) {
  const pkg = {
    name: projectName,
    private: true,
    version: "0.0.0",
    type: "module",
    scripts: {
      dev: "vite",
      build: "vite build",
      preview: "vite preview"
    },
    dependencies: {
      react: "^18.3.1",
      "react-dom": "^18.3.1"
    },
    devDependencies: {
      vite: "^5.4.0",
      "@vitejs/plugin-react": "^4.3.0"
    }
  };

  const mainExt = useTypeScript ? "tsx" : "jsx";
  const appExt = useTypeScript ? "tsx" : "jsx";
  const viteConfig = `import { defineConfig } from "vite";
import react from "@vitejs/plugin-react";

export default defineConfig({
  plugins: [react()]
});
`;

  const indexHtml = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>${projectName}</title>
  </head>
  <body>
    <div id="root"></div>
    <script type="module" src="/src/main.${mainExt}"></script>
  </body>
</html>
`;

  const mainSource = `import React from "react";
import ReactDOM from "react-dom/client";
import App from "./App.${appExt}";
import "./index.css";

ReactDOM.createRoot(document.getElementById("root")).render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
`;

  const appSource = `export default function App() {
  return (
    <main style={{ padding: 16, fontFamily: "system-ui, sans-serif" }}>
      <h1>${projectName}</h1>
      <p>Generated by kcm.</p>
    </main>
  );
}
`;

  const css = `:root {
  color-scheme: light dark;
}

body {
  margin: 0;
}
`;

  await fs.mkdir(path.join(projectDir, "src"), { recursive: true });

  if (useTypeScript) {
    pkg.devDependencies.typescript = "^5.6.0";
    pkg.devDependencies["@types/react"] = "^18.3.0";
    pkg.devDependencies["@types/react-dom"] = "^18.3.0";
  }

  await fs.writeFile(path.join(projectDir, "package.json"), JSON.stringify(pkg, null, 2) + "\n", "utf8");
  await fs.writeFile(path.join(projectDir, "vite.config.js"), viteConfig, "utf8");
  await fs.writeFile(path.join(projectDir, "index.html"), indexHtml, "utf8");
  await fs.writeFile(path.join(projectDir, "src", `main.${mainExt}`), mainSource, "utf8");
  await fs.writeFile(path.join(projectDir, "src", `App.${appExt}`), appSource, "utf8");
  await fs.writeFile(path.join(projectDir, "src", "index.css"), css, "utf8");

  if (useTypeScript) {
    const tsconfig = `{
  "compilerOptions": {
    "target": "ES2020",
    "useDefineForClassFields": true,
    "lib": ["ES2020", "DOM", "DOM.Iterable"],
    "module": "ESNext",
    "skipLibCheck": true,
    "moduleResolution": "Bundler",
    "resolveJsonModule": true,
    "isolatedModules": true,
    "noEmit": true,
    "jsx": "react-jsx",
    "strict": true
  },
  "include": ["src"]
}
`;
    const envDts = `/// <reference types="vite/client" />
`;
    await fs.writeFile(path.join(projectDir, "tsconfig.json"), tsconfig, "utf8");
    await fs.writeFile(path.join(projectDir, "src", "vite-env.d.ts"), envDts, "utf8");
  }

  await fs.writeFile(path.join(projectDir, ".gitignore"), "node_modules\n.DS_Store\ndist\n.env\n", "utf8");
}

